const month = 7;

switch (month) {
  case 1:
    console.log(`January`);
    break;
  case 2:
    console.log(`february`);
    break;
  case 3:
    console.log(`March`);
    break;
  case 4:
    console.log(`April`);
    break;
  case 5:
    console.log(`May`);
    break;
  case 6:
    console.log(`Jun`);
    break;
  case 7:
    console.log(`july`);
    break;
  case 8:
    console.log(`August`);
    break;
  default:
    console.log(`Error!!`);
    break;
}
